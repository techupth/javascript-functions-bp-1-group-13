let add = function(a,b){
    return a+b
}
let add1 = add(4,2)
console.log(add1) //6
let subtract = function(a,b){
    return a-b
}
let subtract1 = subtract(120, 4)
console.log(subtract1) //116
let multiply = function(a,b){
    return a*b
}
let multiply1 = multiply(45, 25)
console.log(multiply1) //1125
let divide = function(a,b){
    return a/b
}
let divide1 = divide(1235, 5)
console.log(divide1) //247