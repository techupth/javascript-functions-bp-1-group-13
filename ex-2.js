let add = function (a,b){
    let result = a+b;
    return result;
}

let subtract = function (a,b){
    let result = a-b;
    return result;
}

let multiply = function (a,b){
    let result = a*b;
    return result;
}

let divide = function(a,b){
    let result = a/b;
    return result;
}

console.log(add(8,4))
console.log(subtract(8,4))
console.log(multiply(8,4))
console.log(divide(8,4))
