let add = (a,b) => {
    return a+b
}
let add1 = add(1,2)
console.log(add1) //3
let subtract = (a,b) => {
    return a-b
}
let subtract1 = subtract(5, 4)
console.log(subtract1) //1
let multiply = (a,b) => {
    return a*b
}
let multiply1 = multiply(9, 25)
console.log(multiply1) //225
let divide = (a,b) => {
    return a/b
}
let divide1 = divide(500, 5)
console.log(divide1) //100