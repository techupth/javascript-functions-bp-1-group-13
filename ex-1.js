function add(a,b){
    let result = a+b;
    return result;
}

function subtract(a,b){
    let result = a-b;
    return result;
}

function multiply(a,b){
    let result = a*b;
    return result;
}

function divide(a,b){
    let result = a/b;
    return result;
}

console.log(add(8,4))
console.log(subtract(8,4))
console.log(multiply(8,4))
console.log(divide(8,4))
